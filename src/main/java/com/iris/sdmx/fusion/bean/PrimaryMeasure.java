package com.iris.sdmx.fusion.bean;

import java.io.Serializable;

/**
 * @author sajadhav
 *
 */
public class PrimaryMeasure implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 311048083786571585L;

	private String id;

	private String urn;

	private String concept;

	private Representation representation;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the urn
	 */
	public String getUrn() {
		return urn;
	}

	/**
	 * @param urn the urn to set
	 */
	public void setUrn(String urn) {
		this.urn = urn;
	}

	/**
	 * @return the concept
	 */
	public String getConcept() {
		return concept;
	}

	/**
	 * @param concept the concept to set
	 */
	public void setConcept(String concept) {
		this.concept = concept;
	}

	/**
	 * @return the representation
	 */
	public Representation getRepresentation() {
		return representation;
	}

	/**
	 * @param representation the representation to set
	 */
	public void setRepresentation(Representation representation) {
		this.representation = representation;
	}

	/**
	 *
	 */
	@Override
	public String toString() {
		return "PrimaryMeasure [id=" + id + ", urn=" + urn + ", concept=" + concept + ", representation=" + representation + "]";
	}

}
