package com.iris.sdmx.fusion.bean;

import java.util.List;

/**
 * @author apagaria
 *
 */
public class Keys {

	private List<Keyvalues> keyValues;

	/**
	 * @return the keyValues
	 */
	public List<Keyvalues> getKeyValues() {
		return keyValues;
	}

	/**
	 * @param keyValues the keyValues to set
	 */
	public void setKeyValues(List<Keyvalues> keyValues) {
		this.keyValues = keyValues;
	}

	@Override
	public String toString() {
		return "Keys [keyValues=" + keyValues + "]";
	}

}