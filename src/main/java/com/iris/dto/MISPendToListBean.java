package com.iris.dto;

public class MISPendToListBean {
	Long entityId;
	Long returnId;
	Long userRoleMasterId;
	Long userRoleId;
	String companyEmail;
	boolean isSendMail = false;

	public Long getEntityId() {
		return entityId;
	}

	public void setEntityId(Long entityId) {
		this.entityId = entityId;
	}

	public Long getReturnId() {
		return returnId;
	}

	public void setReturnId(Long returnId) {
		this.returnId = returnId;
	}

	public Long getUserRoleMasterId() {
		return userRoleMasterId;
	}

	public void setUserRoleMasterId(Long userRoleMasterId) {
		this.userRoleMasterId = userRoleMasterId;
	}

	public Long getUserRoleId() {
		return userRoleId;
	}

	public void setUserRoleId(Long userRoleId) {
		this.userRoleId = userRoleId;
	}

	public String getCompanyEmail() {
		return companyEmail;
	}

	public void setCompanyEmail(String companyEmail) {
		this.companyEmail = companyEmail;
	}

	public boolean isSendMail() {
		return isSendMail;
	}

	public void setSendMail(boolean isSendMail) {
		this.isSendMail = isSendMail;
	}

}
