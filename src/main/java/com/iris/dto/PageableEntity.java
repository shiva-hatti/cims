package com.iris.dto;

import java.util.List;

import com.iris.model.EntityBean;

/**
 * Wrapper class of the entity bean to hold pageAble data
 * 
 * @author svishwakarma
 *
 */
public class PageableEntity {

	private long totalCount;
	private List<EntityBean> content;

	public long getTotalCount() {
		return totalCount;
	}

	public void setTotalCount(long totalCount) {
		this.totalCount = totalCount;
	}

	public List<EntityBean> getContent() {
		return content;
	}

	public void setContent(List<EntityBean> list) {
		this.content = list;
	}

}
